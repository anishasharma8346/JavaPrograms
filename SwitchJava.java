package practice_package;

public class SwitchJava {

	public static void main(String[] args) {

		switch(3)

		{
		case 1:
			System.out.println("Case 1 execution");
			break;

		case 2:
			System.out.println("Case 2 execution");
			break;

		case 3:
			System.out.println("Case 3 execution");
			break;

		case 4:
			System.out.println("Case 4 execution");
			break;

		case 5:
			System.out.println("Case 5 execution");
			break;

		default:
			System.out.println("Selection is default selection");
		}
	}

}
